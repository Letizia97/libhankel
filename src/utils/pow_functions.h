
#ifndef POW_FUNCTIONS_H
#define POW_FUNCTIONS_H

static inline double pow2(double x) { return x * x; }

static inline double pow3(double x) { return x * x * x; }

static inline double pow4(double x) { return x * x * x * x; }

static inline double pow5(double x) { return x * x * x * x * x; }

static inline double pow6(double x) { return x * x * x * x * x * x; }

static inline double pow7(double x) { return x * x * x * x * x * x * x; }

#endif // POW_FUNCTIONS_H
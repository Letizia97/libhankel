def sf_poch(a, x):
    return math.gamma(a + x) / math.gamma(a)


def dab(q, params):
    """
    Example Debye-Anderson-Brumberger (DAB) model.
    """
    XI = params[0]
    H = params[1]
    ETA = params[2]

    factor1 = (2 * XI) ** 3
    factor2 = sf_poch(H, 1.5)

    numer = (factor1 * factor2 * ETA) ** 2 * (np.pi**3)
    denom = (1 + (q * XI) ** 2) ** (1.5 + H)

    return numer / denom


